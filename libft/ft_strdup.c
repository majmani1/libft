#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		l;
	char	*str;
	int		i;

	i = 0;
	l = ft_strlen(src);
	str = malloc(sizeof(const char) * l + 1);
	if (str == 0)
		return (0);
	while (src[i] != '\0')
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
